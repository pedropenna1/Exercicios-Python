import math
angulo = float(input('Digite um ângulo: '))
print('O valor do cosseno deste ângulo é:{:.2f}'.format(math.cos(math.radians(angulo))))
print('O valor do seno deste ângulo é:{:.2f} '.format(math.sin(math.radians(angulo))))
print('O valor da tangente deste ângulo é:{:.2f}'.format(math.tan(math.radians(angulo))))

